package ideias;

public class OrshaBar {
	Cliente convidado;
	private int skol;
	private int brahma;
	
	public Cliente getConvidado() {
		return convidado;
	}
	public void setConvidado(Cliente convidado) {
		this.convidado = convidado;
	}
	public int getSkol() {
		return skol;
	}
	public void setSkol(int skol) {
		this.skol = skol;
	}
	public int getBrahma() {
		return brahma;
	}
	public void setBrahma(int brahma) {
		this.brahma = brahma;
	}
}
